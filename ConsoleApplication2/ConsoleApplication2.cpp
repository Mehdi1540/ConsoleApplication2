#include <iostream>
using namespace std;
int main()
{
	unsigned short int num1;

	cout << "Please enter a number: ";  cin >> num1; cout << "\n";


	cout << " Number^2 = " << num1 * num1 << endl;
	cout << " Number^3 = " << num1 * num1 * num1 << endl;
	cout << " Number^4 = " << num1 * num1 * num1 * num1 << endl;
}